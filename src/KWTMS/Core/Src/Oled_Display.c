void SSD1306_DisplayTemperature(float temp)
{
    uint8_t buffer1[128];
    uint8_t buffer2[128];

    memset(buffer1,0,128);
    memset(buffer2,0,128);

    char title[] = "TEMPERATURE";

    int t1 = (int)temp;
    int t2 = (int)((temp - t1)*100);

    char value[20];
    sprintf(value,"%d.%02d C",t1-4,t2-4);

    int len1 = strlen(title);

    for(int i=0;i<len1;i++)
    {
        char c = title[i];
        int index = -1;

        if(c==' ') index=0;
        else if(c>='A' && c<='Z') index=(c-'A')+11;

        int pos = 10 + (i*11);

        if(index>=0)
        {
        	for(int j=0;j<5;j++)
        	{
        	    buffer1[pos+(j*2)] = font5x7[index][j];
        	    buffer1[pos+(j*2)+1] = font5x7[index][j];
        	}
        }
    }

    int len2 = strlen(value);

    for(int i=0;i<len2;i++)
    {
        char c = value[i];
        int index = -1;

        if(c==' ') index=0;
        else if(c>='0' && c<='9') index=(c-'0')+1;
        else if(c=='.') index=38;
        else if(c=='C') index=('C'-'A')+11;

        int pos = 35 + (i*11);

        if(index>=0)
        {
        	for(int j=0;j<5;j++)
        	{
        	    buffer2[pos+(j*2)] = font5x7[index][j];
        	    buffer2[pos+(j*2)+1] = font5x7[index][j];
        	}
        }
    }

    /* title */
    SSD1306_WriteCommand(0xB2);
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x10);
    SSD1306_WriteData(buffer1,128);

    /* temperature value */
    SSD1306_WriteCommand(0xB4);
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x10);
    SSD1306_WriteData(buffer2,128);
}
