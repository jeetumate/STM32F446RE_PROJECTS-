void SSD1306_DisplayImage(const uint8_t *img)
{
    for(uint8_t page = 0; page < 8; page++)
    {
        SSD1306_WriteCommand(0xB0 + page);
        SSD1306_WriteCommand(0x00);
        SSD1306_WriteCommand(0x10);

        SSD1306_WriteData((uint8_t*)&img[page * 128], 128);
    }
}
void SSD1306_Clear(void)
{
    uint8_t clear[128];
    for(int i=0;i<128;i++) clear[i]=0x00;

    for(uint8_t page=0; page<8; page++)
    {
        SSD1306_WriteCommand(0xB0 + page);
        SSD1306_WriteCommand(0x00);
        SSD1306_WriteCommand(0x10);

        SSD1306_WriteData(clear,128);
    }
}
void SSD1306_StartScroll(void)
{
    SSD1306_WriteCommand(0x2E); // stop scroll first

    SSD1306_WriteCommand(0x26); // horizontal scroll right
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x00); // start page = 0
    SSD1306_WriteCommand(0x00); // speed
    SSD1306_WriteCommand(0x07); // end page = 7
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0xFF);

    SSD1306_WriteCommand(0x2F); // activate scroll
}
void SSD1306_DisplayMAC(void)
{

    uint8_t buffer[128];
    memset(buffer,0,128);

    char text1[] = "MAC ID:";
    char text2[] = "AA:BB:CC:DD:EE:FF";

    int len = strlen(text1);

    for(int i=0;i<len;i++)
    {
        char c = text1[i];
        int index = -1;

        if(c==' ') index=0;
        else if(c>='A' && c<='Z') index=(c-'A')+11;

        int pos = 10 + (i*11);

        if(index>=0)
        {
        	for(int j=0;j<5;j++)
        	{
        	    buffer[pos+(j*2)] = font5x7[index][j];
        	    buffer[pos+(j*2)+1] = font5x7[index][j];
        	}
        }
    }

    SSD1306_WriteCommand(0xB2);
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x10);
    SSD1306_WriteData(buffer,128);
}
void SSD1306_DrawScrollingText(void)
{
    uint8_t line[128];
    uint8_t buffer[128];

    for(int i=0;i<128;i++)
    {
        line[i] = 0x60;     // horizontal separator
        buffer[i] = 0x00;
    }

    int len = strlen(scrollText);

    for(int i=0;i<len;i++)
    {
        char c = scrollText[i];
        int index = -1;

        if(c==' ')
            index = 0;
        else if(c>='0' && c<='9')
            index = (c-'0') + 1;
        else if(c>='A' && c<='Z')
            index = (c-'A') + 11;
        else if(c>='a' && c<='z')
            index = (c-'a') + 11;
        else if(c=='|')
            index = 37;
        else if(c=='.')
            index = 38;
        else if(c=='+')
            index = 39;

        int pos = scrollX + (i*6);

        if(index >= 0 && index < (sizeof(font5x7)/sizeof(font5x7[0])))
        {
            for(int j=0;j<5;j++)
            {
                int drawPos = pos + j;

                if(drawPos >=0 && drawPos <128)
                {
                	buffer[drawPos] = font5x7[index][j];

                	if(drawPos+1 < 128)
                	{
                	    buffer[drawPos+1] = font5x7[index][j];
                	}
                }
            }
        }
    }

    // draw separator line
    SSD1306_WriteCommand(0xB6);
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x10);
    SSD1306_WriteData(line,128);

    // draw scrolling text
    SSD1306_WriteCommand(0xB7);
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x10);
    SSD1306_WriteData(buffer,128);

    scrollX--;

    if(scrollX < -(len*6))
        scrollX = 128;
}
